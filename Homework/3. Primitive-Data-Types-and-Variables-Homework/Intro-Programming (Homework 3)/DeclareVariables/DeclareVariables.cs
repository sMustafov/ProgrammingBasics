using System;


namespace DeclareVariables
{
    class DeclareVariables
    {
        static void Main(string[] args)
        {
            sbyte a = -115;
            ushort b = 52130;
            short c = -10000;
            byte d = 97;
            uint e = 4825932;
            Console.WriteLine("{0}\n{1}\n{2}\n{3}\n{4}", a, b, c, d, e);
            Console.ReadKey();
        }
    }
}
